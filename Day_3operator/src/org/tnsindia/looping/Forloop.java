package org.tnsindia.looping;

import java.util.Scanner;

public class Forloop {

	public static void main(String[] args) {
		Scanner s =new Scanner(System.in);
		int n =s.nextInt();
		for(int i=n;i<=1000;i++)
		{
			System.out.println(i +" ");
			s.close();
			
		}
		
		
		
		
		
		
		
	}
}
